package AccessModifier.mypack1;


public class demo1 {
    int a = 10;
    public int b = 20;
    private int c = 30;
    protected int d = 40;

    public void show() {
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
    }
    
}
